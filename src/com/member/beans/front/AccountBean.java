package com.member.beans.front;

public class AccountBean {
	private String kindData;
	private String createTime;
	private String project;
	private String income;
	private String pay;
	private String pointbalance;
	private String goldmoneybalance;
	private String redmin;
	
	public String getKindData() {
		return kindData;
	}
	public void setKindData(String kindData) {
		this.kindData = kindData;
	}
	
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getIncome() {
		return income;
	}
	public void setIncome(String income) {
		this.income = income;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getPointbalance() {
		return pointbalance;
	}
	public void setPointbalance(String pointbalance) {
		this.pointbalance = pointbalance;
	}
	public String getGoldmoneybalance() {
		return goldmoneybalance;
	}
	public void setGoldmoneybalance(String goldmoneybalance) {
		this.goldmoneybalance = goldmoneybalance;
	}
	public String getRedmin() {
		return redmin;
	}
	public void setRedmin(String redmin) {
		this.redmin = redmin;
	}
	
	

}
