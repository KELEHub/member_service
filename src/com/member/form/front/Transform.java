package com.member.form.front;

public class Transform {
	
	private String toUserNumber;
	private String toUserName;
	private String payPassword;
	private String toGoldMoney;

	public String getPayPassword() {
		return payPassword;
	}

	public void setPayPassword(String payPassword) {
		this.payPassword = payPassword;
	}

	public String getToGoldMoney() {
		return toGoldMoney;
	}

	public void setToGoldMoney(String toGoldMoney) {
		this.toGoldMoney = toGoldMoney;
	}

	public String getToUserNumber() {
		return toUserNumber;
	}

	public void setToUserNumber(String toUserNumber) {
		this.toUserNumber = toUserNumber;
	}

	public String getToUserName() {
		return toUserName;
	}

	public void setToUserName(String toUserName) {
		this.toUserName = toUserName;
	}
	

}
