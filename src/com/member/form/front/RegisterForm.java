package com.member.form.front;

public class RegisterForm {
	
	
	private String bankProvince;
	private String bankCity;
	private String bankCounty;
	private String number;
	private String refereeNumber;
	private String username;
	private String identity;
	private String phoneNumber;
	private String bankname;
	private String bankCard;
	private String bankAddress;
	public String getBankProvince() {
		return bankProvince;
	}
	public void setBankProvince(String bankProvince) {
		this.bankProvince = bankProvince;
	}
	public String getBankCity() {
		return bankCity;
	}
	public void setBankCity(String bankCity) {
		this.bankCity = bankCity;
	}
	public String getBankCounty() {
		return bankCounty;
	}
	public void setBankCounty(String bankCounty) {
		this.bankCounty = bankCounty;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getRefereeNumber() {
		return refereeNumber;
	}
	public void setRefereeNumber(String refereeNumber) {
		this.refereeNumber = refereeNumber;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getIdentity() {
		return identity;
	}
	public void setIdentity(String identity) {
		this.identity = identity;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getBankname() {
		return bankname;
	}
	public void setBankname(String bankname) {
		this.bankname = bankname;
	}
	public String getBankCard() {
		return bankCard;
	}
	public void setBankCard(String bankCard) {
		this.bankCard = bankCard;
	}
	public String getBankAddress() {
		return bankAddress;
	}
	public void setBankAddress(String bankAddress) {
		this.bankAddress = bankAddress;
	}
	

}
