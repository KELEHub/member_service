package com.member.form.back;

public class IntegralHistoryForm {
	
	private String userNumber;
	private String project;
	private String income;
	private String pay;
	private String pointbalance;
	private String createTime;
	
	private String points;
	private String serverpoints;
	private String totalpoints;
	
	public String getUserNumber() {
		return userNumber;
	}
	public void setUserNumber(String userNumber) {
		this.userNumber = userNumber;
	}
	public String getProject() {
		return project;
	}
	public void setProject(String project) {
		this.project = project;
	}
	public String getIncome() {
		return income;
	}
	public void setIncome(String income) {
		this.income = income;
	}
	public String getPay() {
		return pay;
	}
	public void setPay(String pay) {
		this.pay = pay;
	}
	public String getPointbalance() {
		return pointbalance;
	}
	public void setPointbalance(String pointbalance) {
		this.pointbalance = pointbalance;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getPoints() {
		return points;
	}
	public void setPoints(String points) {
		this.points = points;
	}
	public String getServerpoints() {
		return serverpoints;
	}
	public void setServerpoints(String serverpoints) {
		this.serverpoints = serverpoints;
	}
	public String getTotalpoints() {
		return totalpoints;
	}
	public void setTotalpoints(String totalpoints) {
		this.totalpoints = totalpoints;
	}
	

}
