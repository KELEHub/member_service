package com.member.form.back;

public class DeclarationForm {
	
	private String declarationIndex;
	private String maxDeclaration;
	private String declarationAll;
	public String getDeclarationIndex() {
		return declarationIndex;
	}
	public void setDeclarationIndex(String declarationIndex) {
		this.declarationIndex = declarationIndex;
	}
	public String getMaxDeclaration() {
		return maxDeclaration;
	}
	public void setMaxDeclaration(String maxDeclaration) {
		this.maxDeclaration = maxDeclaration;
	}
	public String getDeclarationAll() {
		return declarationAll;
	}
	public void setDeclarationAll(String declarationAll) {
		this.declarationAll = declarationAll;
	}
	
	
	

}
